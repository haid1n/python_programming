class Date:
	# Constructor method for attributes
	def __init__(self, day, month, year):
		# Instance attribute
		self.day = day

		self.month = month

		self.year = year

	# Method for giving out the year
	def year(self):
		return self.year

	# Method for giving out the month
	def month(self):
		return self.month

	# Method for giving out the day
	def day(self):
		return self.day 